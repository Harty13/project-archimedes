<h2>Monthly prices in Angoul&ecirc;me, 1819-1880</h2>

<h5>Author: Fr&eacute;d&eacute;ric Michaud</h5>

<p>
<strong>The datafile:</strong> <a href="angouleme.xls">spreadsheet</a>, <span class="small">(.xls, 66 Kb)</span></p>

<p>The data on Angoul&ecirc;me were obtained from:<br>
"Mercuriales d'Angoul&ecirc;me", in: <em>Annales de la Soci&eacute;t&eacute; d'agriculture, arts et commerce du d&eacute;partement de la Charente</em>. These are also available through the search engine (use: <em>recherche</em>) of <a href="http://gallica.bnf.fr">Gallica, biblioth&egrave;que num&eacute;rique de la Biblioth&egrave;que nationale de France</a>.</p>

<p>This datafile contains monthly price quotations for the following goods:</p>
<pre>
Wheat			100 litres
Dry beans		100 litres
White bread		1 kg
Beef			1 kg
Veal			1 kg
Mutton			1 kg
Pork			1 kg
Straw			100 kg
Red wine		2 litres
</pre>

<p>Prices are quoted in French francs. The franc contained 4.5 grams of silver and was subdivided into 100 centimes.</p>

<p>Data for the following years were missing: 1827-1829, 1831, 1833-1838, 1840-1842, 1866-1868, 1875-1876.</p>

