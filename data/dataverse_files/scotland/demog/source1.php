<h2>Scottish Economic History Database</h2>	
<h4>Edinburgh Mortality Data; Source Notes</h4>

<p>
This data has been transcribed from the monthly abstract of Edinburgh Bills of Mortality published in the Scots Magazine, 1740 - 1792.  There is no information given in the Scots Magazine concerning this data except for the burial grounds covered by each abstract (see below).</p>

<p>
Subject to all the usual problems associated with the use of Bills of Mortality (see Flinn, M.W. (ed), Scottish population history from the seventeenth century to the 1930s (Cambridge, 1977), pp73-7), care must also be taken with the changing area covered by the Bills.  Thus the following churchyards were apparently included during the given periods;</p>

<ul>
<li>1740 - 1776; City and West Kirk </li>
<li>1777 - 1786; City</li>
<li>1787 - 1792; City, West Kirk, Cannongate and Carlton</li>
</ul>

