<h2>Scottish Economic History Database</h2>	
<h4>User Notes</h4>

<p>
This database has been produced to further the dissemination of data collected during a project on Scottish wages and prices, 1550 -1780, funded by the British Economic and Social Research Council (Reference B00340067).  The broader results of the project are presented in AJS Gibson and TC Smout, <I>Scottish food, wages and prices, 1550 - 1780</I> (Cambridge University Press, 1994), as well as in a number of <a href="papers.php">papers addressing particular issues.</a></p>

<p>
This database is made freely available, with the condition that its use in any published work is accompanied by a reference to its URL (<a href="//www.iisg.nl/hpw/scotland/">www.iisg.nl/hpw/</a>).</p>

<p>
Prospective users should note that this database aims only to provide ready access to the raw data, and issues such as the veracity of the data and the significance of changing systems of weights and measures are not fully addressed. The authors recommend, therefore, that users consult the aforementioned book for a full discussion of the dataset and its use.</p>

<p>
The most convenient way to explore the nature of the data available, or to obtain particular data series, is to search the database using the heirachical menu system. Extensive cross-linking should ensure that the process is fast and efficient. The actual data files obtained should be largely self-explanatory, although in some instances reference is made to general or specific source notes also held as part of this database.</p>

<p>
As a general rule, each file commences with a series of cross-references to other files from the same source or place.  There then follows a short general description of the contents of the file, a note on the source of the data, and finally a list of the particular series. As these files were originally conceived to be accessed and manipulated using a dedicated FORTRAN-based data-management program, there is also some coded information describing the contents of the file. <a href="codes.php">A full description of the codes used</a> is provided as in some instances it may be necessary to use this to fully understand the contents of the file.</p>

