<h2>Scottish Economic History Database</h2>
<h4>The Pittenweem Sea Box Book, 1658-1755; Source Notes</h4>
<p>
The prices are those at which bear and meal were sold by the masters and keepers of the sea box to provide income for charity.  The bear is described as 'ferme bear', the meal is 'ferme', or sometimes 'fine', oatmeal.<br><br>

The grain was received as rent in kind from lands round Pittenweem, Balcaskie, Overkellie, etc.<br><br>

Sometimes more than one sale price is stated. Since no actual sale dates are given (only the crop date), the price extracted is that for the largest quantity sold.</p>

