<h2>Scottish Economic History Database</h2>
<h4>Edinburgh Monthly Meal Prices, 1741 - 1796; Source Notes</h4>
The Scots Magazine monthly market reports for Edinburgh are found in each issue of the paper.  These report the retail price of various grains and wheatbread.  Given in pence sterling in the documents, recorded here as shillings Scots.
<p>
Other bread prices are occasionally available but are not recorded here.
<p>
For further information on monthly prices quotation see Gibson & Smout, Prices, food and wages in Scotland, 1550 - 1780, Ch. 4.

