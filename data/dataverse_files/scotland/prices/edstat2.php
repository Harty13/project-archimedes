       Scottish town councils in regulating prices in the market through
       such statutes see Gibson & Smout, Prices, food and wages in Scotland,
       1550 - 1780 (CUP, 1994), Ch. 2.

 Also see <prices/edstat1>, <prices/edstat3> and <prices/edstat4> for
