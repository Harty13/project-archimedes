<h2>Scottish Economic History Database</h2>
<h4>Stirlingshire Fiars; Source Notes</h4>

<p>
Although not extracted, Scottish Record Office, SC 67/28/1 also gives fiars prior to 1774.<br>
From 1782 to 1791 inclusive, wheat prices are per Linlithgow boll.  In 1782 pease and beans are per Stirlingshire boll.  The oatmeal boll was of 8 stones Scottish Troy (Dutch Weight).</p>

<p>
SC 67/28/2 notes the relationship between the Stirlingshire boll measures and the Linlithgow Standard:</p>
<pre>
    Oats & Barley;  Stirling boll = 1b. 1p. 1/4l. Linlithgow bolls
                         (i.e. = 1.1328 Linlithgow bolls)
    Wheat & Pease; Stirling boll = 1b. 1p. 2l. 1/4 gill Linlithgow bolls
                         (i.e. = 1.28125 (+1/4 gill) Linlithgow bolls)
</pre>
