<h2>Scottish Economic History Database</h2>
<h4>Midlothian (Edinbughshire) Fiars, 1626 - 1931; Source Notes</h4>
<p>Sources:</p>

<ul>
<li>1626 - 1779; Bald, A., The farmer and corn dealer's assistant (Edinburgh, 1780).  Also Scottish Record Office, GD 69/282</li>
<li>Oatmeal, 1780 - 1826; Appendix B of Flinn, M.W. (ed.), Scottish population history from the seventeenth century to the 1930s (Cambridge, 1977). These in turn were taken from the Scots Magazine and Flinn notes that "they should be used with care since in some cases they relate to bulk measures, in others to weight".  As this is a problem common to most fiars prices they are probably no less reliable.)</li>
<li>Wheat, bear, meal and oats, 1780 - 1931; 'Fiars prices of victual of Lothian boll of growth of the county of Edinburgh, 1640-1931', Scottish Record Office, GD 224/1066; a copy of which to 1813 as Scottish Record Office, RH 2/4/109.</li>
</ul>

<p>Also available, but not used to create the prices series, are;</p>

<ul>
<li>The sheriff court fiars' records, 1821-onwards; Scottish Record Office, SC 39/90</li>
<li>Loch of Drylaw mss., 1750 - 1770; Scottish Record Office, GD 268/7/2</li>
</ul>

<p>From 1696 to 1826 the boll oatmeal is of 8 stone Scottish Troy.  From 1827 Imperial standards were introduced.  Prices of wheat, bear and oats were given per bushel from 1827 - 1836 (converted on the basis that one bushel equals 0.25 boll) and per quarter from 1837 to 1931 (converted on the basis that one quarter equals 2.0 bolls).  Oatmeal prices were given per hundred-weight of 112 lbs. avoirdupois from 1827 to 1931.<br><br>

