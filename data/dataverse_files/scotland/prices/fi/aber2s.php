<h2>Scottish Economic History Database</h2>
<h4>Aberdeenshire fiars; Source Notes</h4>
<p>From: Littlejohn, D., 'Aberdeenshire fiars', Miscellany of the New Spalding Club, vol. 2, (New Spalding Club, Aberdeen, 1906), pp.2-43.<br><br>

Ferme and mercat meall were listed seperately until 1651, therafter they are given together with a single price until 1717 when the source refers to farm meall only.  (I have nevertheless continued to place these prices in the mercat meall column as well as the ferme meall column.  From 1771 the boll of ferme meall is said to be 8 stone, although whether this is a change or not is uncertain.<br><br>

White meall is described as being 9 stone the boll from 1741, although once again it is uncaetain whether this is a change or not.<br><br>

Ferme and mercat bear were listed seperately until 1655, thereafter they are given together with a single price.<br><br>

Prior to 1724 there were often two courts held each year and the fiars' prices for a given crop year sometimes varied between them.  From 1724 there was only one court held annually, in or soon after February, as fixed by the act of sederunt of 21st December, 1723.<br><br>

