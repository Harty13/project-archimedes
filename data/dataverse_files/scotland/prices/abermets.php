<h4>The St. Nicollas Met and Sysboll Levy, Aberdeen, 1631 - 1670; Source Notes</h4>

<p>
The accounts run from Martinmas to Martinmas, and the dates given refer to the second of the two calender years covered by each account.  Thus, prices recorded in the account from Mart. 1666 to Mart. 1667 is listed under the calender year 1667.</p>

<p>
"a .... levy on non-freemen for grain and salt, and on all merchants for various other commodities was the syseboll, one of the petty customs which the compter began to collect in 1630/1.  This might be paid literally as a syseboll of the commodity, or in cash at the rate at which the cargo was sold." (L.B. Taylor, p.7)</p>

<p>
The St. Nicholas mett was the direct equivalent as paid by the burgesses of the town of Aberdeen.</p>

<p>
These prices are believed to continue into 18th century, and to be available in mss at Aberdeen Town House archives.</p>

