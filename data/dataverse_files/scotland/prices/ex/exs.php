<h2>Scottish Economic History Database</h2>
<h4>The Exchquer Rolls; Source Notes</h4>
<p>There are two series within the Exchequer Rolls that have been used to obtain commodity prices for the 15th and 16th centuries; </p>

<ul>
<li>The accounts of the Ballivi ad Extra comprise the submissions of the accountants responsible for the collection of rents and produce due from the Crown's demesne.  Whilst many valuations are to be found in these accounts, only when there has been an explicit sale is it likely that the values are anything other than fictitious.  In these cases the accountant absolves himself of the responsibility of delivering the goods by noting what had been sold and what price was obtain.  It was this money that was then due to the Comptroller.</li>
<li>The Comptrollers' sccounts were general statements concerning the Crown's income and expenditure and in some cases include references to the sale of goods recieved from the Crown's demsesne.  Prices were often given and tend to correspond with those noted in the Ballivi ad Extra.  The Comptrollers' accounts are thus particularly useful when the Ballivi ad Extra accounts are wanting.</li>
</ul>

<p>
There may still be some artificiality in the price series as some goods were sold to the same individuals/institutions year after year.  Particularly, but not only, when the person concerned was that from whom the commodity was to be collected in the first place it is possible that some favourable contact price had been agreed.  When that is obviously the case the 'transaction' has been ignored.</p>

<p>
The prices covering 1325-1465 taken from Grant, A., Independence and Nationhood: Scotland, 1306 - 1469 (Edward Arnold, London, 1984) have been ascribed to crop years here rather than, as he had them (I presume), calender years.  This may not have been appropriate.</p>

<p>
There is some discussion of 16th century prices in general and the Exchequer records in particular in chapter 1 of Gibson and Smout, Prices, food and wages in Scotland, 1550 - 1780 (CUP, 1994), but for a full discussion of early Scottish price history refer to Nick Meyhew's forthcoming book.</p>

