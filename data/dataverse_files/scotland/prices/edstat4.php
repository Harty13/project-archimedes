 From: Marwick, J.D., Wood, M., Hannay, R.K., and Armet, H., Extracts
       from the records of the burgh of Edinburgh, 1403-1701 (13 vols.,
       Edinburgh, 1869-1967).

  This file contains;

  1 - Edinburgh tallow prices (per stone); 1562 -1693.

 Note: As statute (assize) prices these were meant to determine the 
       maximum price at which the products of craftwork could be sold 
       in the burgh.  For a discussion of the likely effectiveness of 
       Scottish town councils in regulating prices in the market through
       such statutes see Gibson & Smout, Prices, food and wages in Scotland,
       1550 - 1780 (CUP, 1994), Ch. 2.

 Also see <prices/edstat1>, <prices/edstat2> and <prices/edstat3> for
 Edinburgh town council price statutes for ale, wheatbread and candle
 respectively.

