<h2>Scottish Economic History Database</h2>
<h4>Buchanan estate labourers' wages, 1722 - 1787; Source notes</h4>

<p>These wages are from actual accounts and, as described in chapter 8 of Gibson & Smout, Prices, food and wages in Scotland, 1550 - 1780 (CUP, 1994), the series attempt to portray the 'going rate' for each occupation.<br><br>

For further detailed discussion of labours at Buchanan, see Gibson, A., 'Proletarianization?  The transition to full-time labour on a Scottish estate, 1723-1787', Continuity and Change, Vol. 5(3), 1990, pp.357-389.</p>

<h5>Important Note:</h5>
<p>The wage-rates reported below have been extracted from accounts which follow the crop year.  As they are summer rates, the wages actually refer to the summer of the year @following@ that given below.  Thus, for example, a summer rate of 7s. Scots per day was first paid during the calender year 1763.  Table 8.7 in Gibson & Smout, Prices, food and wages in Scotland, 1550 - 1780 (CUP, 1994) is thus in error by one year throughout.</p>
