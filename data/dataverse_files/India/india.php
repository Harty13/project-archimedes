<h2>Monthly cost of living product prices and index in Bombay, 1921-1940</h2>

<p>Due to a change in the format of the data, the data is separated in two datafiles:<br>
<strong>The datafile:</strong><br>
1. <a href="india1921-1937.xls">spreadsheet</a> <span class="small">(.xls, 167 Kb)</span><br>
2. <a href="india1937-1940.xls">spreadsheet</a> <span class="small">(.xls, 286 Kb)</span></p> 

<p>The series of consumer prices is obtained from the <em>Labour Gazette</em>. It is separated in two files due to a change in format in the 1930s. The notes below are also obtained from the <em>Gazette</em>.</p> 

<h5>Sources:</h5>
<p>The first datafile, India cost of living 1921-1937, is obtained from:<br>
<em>Labour gazette</em>, Government of Maharastra: Office of the Commissioner of Labour Bombay, vol I (1921)-Vol XVI (1937, No 10)<br>

The second datafile, India cost of living 1937-1940, is obtained from:<br>
<em>Labour gazette</em>, Government of Maharastra: Office of the Commissioner of Labour Bombay, Vol XVI (1937, No 10)-Vol XX (1940, Feb)</p>

<p>The notes are obtained from:<br>
<em>Labour gazette</em>, Government of Maharastra: Office of the Commissioner of Labour Bombay, Vol. I No. 1 (Sept. 1921) p. 8-11.</p>

<h5>Notes:</h5>
<p>The information as to the predominantly retail prices of food is collected twice weekly between 21st of one month and 20th of the following month at ten ordinary retailers. After each collection, the figures are averaged. At the end of each month, the figures were summed up and dived by eight. Each number therefore consists of 2*10*4 prices. This, however, is not always rigorously applied as for example some retailers have more shops with a uniform price. Kerosene prices are supplied by the Asiatic Petroleum Co, Ltd.<br><br>

To each article, the relative importance in the working class expenditure is attached.  This relative importance number is called 'mass unit'. The mass units were calculated by taking for the five pre World War I years the production plus imports minus exports of the article concerned for all India. To arrive at the aggregate expenditure of this composite unit, the price of each article is multiplied by its own mass-unit.<br><br>

As from 1937 this index was revised. It now constituted more articles. The weights were based on the family budget enquiry in Bombay City between May 1932 and June 1933.</p>


