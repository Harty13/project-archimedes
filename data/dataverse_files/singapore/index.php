<h2>Database Commodity Price data Singapore 1830-1914</h2>

<h5>Author: Maurits van Os</h5>

<p>The price data are taken from the Straits Settlements available in the <i>Koninklijke Bibliotheek</i> in The Hague, the <i>National University of Singapore Central Library</i>, and the <i>Singapore National Library</i>. This extraordinary comprehensive source made it possible to construct a monthly overview of numerous Singapore trading goods. The database can be divided into two parts First, the comprehensive enumeration from 1830 until 1864 from which a selection was made. Secondly, the simplified collection of quotations from 1869 on, which was copied entirely. To facilitate future research Spanish Dollar exchange rate to the Java Guilder were added. For multiple reasons data from a number of editions were not available in the Singapore archives. Possibly sources present in English (and Dutch) archives can complete these series.</p>
&nbsp;&nbsp;The sources used were:<ul>
<li>
From the National university of Singapore Central Library<br>
<u>Newspapers:</u><br>
- <i>Government Gazette, Prince of Wales Island, Singapore and Malacca</i><br>
- <i>Singapore Chronicle & Commercial Register (weekly ed.)</i><br>
- <i>Singapore Free Press & Mercantile Advertiser (weekly ed.)</i><br>
- <i>The Straits Times (daily ed.)</i><br>
<u>Exchange Rates:</u><br>
-	Straits Settlements Statistical office, <i>Blue book for the year</i> ...,  (1872-1914)
</li>
<li>
From the Singapore National library:<br>
<u>Newspapers:</u><br>
- <i>Singapore Free Press & Mercantile Adviser (weekly ed.)</i><br>
- <i>The Straits Times (weekly ed.)</i><br>
- <i>The Straits Times & Singapore Journal of Commerce (weekly ed.)</i><br>
</li>
<li>
From the Koninklijke Bibliotheek in The Hague, The Netherlands:<br>
