<h2>Monthly grain prices in England, 1270-1955</h2>

<p>
<strong>The datafiles:</strong></p>
<ul class="square">
<li>Wheat and malt prices in Winchester (1657-1817), <a href="winchester.xls">spreadsheet</a> <span class="small">(.xls,  36Kb)</span></li>
