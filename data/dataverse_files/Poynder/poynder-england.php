<p>This file contains monthly prices of wheat, barley and oats in English shillings per Winchester quarter (c. 291 litres). The data are used in a paper by Nicholas Poynder, <a href="poynder.pdf">Grain storage in theory and history</a> <span class="small">(.pdf, 40 Kb)</span>. Paper presented at the Third Conference of the European Historical Economics Society, Lisbon, October 29-30, 1999.<br><br>

Other datafiles relating to this paper:<br>
<a href="poynder-france.php">Monthly Grain Prices at Les Halles, Paris. 1549-1698</a><br> 
<a href="poynder-germany.php">Monthly Wheat Prices in Cologne, 1550-1700</a><br>
<a href="poynder-italy.php">Monthly grain prices in Siena, 1550-1694</a></p>

<p>For source references see Table 1 in the paper.</p>

<p>The following Saints' days are used:</p>
<pre>
Michaelmas	  29 September
St. Thomas'	  21 December
Christmas	  25 December
Candlemas	   2 February
Lady Day	  25 March
Midsummer	  24 June
Lammas		   1 August</pre>

<p>Prices are measured in shillings per Winchester quarters of c. 291 litres.<br>
1 quarter = 8 bushels<br>
1 bushel = 4 pecks</p>

<p>The silver content of the English currency can be obtained from: Robert C. Allen and Richard W. Unger, with the assistance of Shannon Parker and Stephanie McWhinnie, <a href="http://www2.history.ubc.ca/unger/CURRCONV/English%20pence.xls">Allen - Unger Database, European Commodity Prices 1260-1914</a> </p>

