<h2>Monthly Grain Prices at Les Halles, Paris. 1549-1698</h2>

<p>
<strong>The datafile:</strong> <a href="poynder-france.xls">spreadsheet</a> <span class="small">(.xls, 64 Kb)</span></p>

<p>
This file contains monthly prices of wheat, barley and oats in Tournois pounds per setier (156 litre). The data are used in a paper by Nicholas Poynder, <a href="poynder.pdf"><em>Grain storage in theory and history</em></a>, <span class="small">(.pdf, 40 Kb)</span>. Paper presented at the Third Conference of the European Historical Economics Society, Lisbon, October 29-30, 1999. </p>

<p>
Other datafile relating to this paper:<br>
<a href="poynder-germany.php">Monthly Wheat Prices in Cologne, 1550-1700</a><br>
<a href="poynder-england.php">Monthly grain prices in England, 1270-1955</a><br>
<a href="poynder-italy.php">Monthly grain prices in Siena, 1550-1694</a></p>

<p>
<strong>About this datafile</strong><br>
The prices are in Tournois pounds per setier (156 litre) and were used in a paper by Nicholas Poynder, <em>Grain storage in theory and history</em>, Presented at the Third Conference of the European Historical Economics Society, Lisbon, October 29-30, 1999. The data theirselves are obtained from  M Baulant and J Meuvret, <em>Prix des cereales extraits de la mercuriale de Paris (1520-1688)</em>: 2 vols. (Paris, 1962). (Vol I and II, p. 4-45 and 45-81).<br><br>

For people wanting to convert these prices in grams of silver, we provide the grams of silver per Tournois pound in the table below.
