<h2>Monthly wheat prices in Siena, 1546-1765</h2>

<p>
<strong>The datafile:</strong> <a href="poynder-italy.xls">spreadsheet</a>, <span class="small">(.xls, 40 Kb)</span></p>

<p>This file contains monthly wheat prices in soldi per staio sense (22.84 litres). Part of the data are used in a paper by Nicholas Poynder, <a href="poynder.pdf">Grain storage in theory and history</a> <span class="small">(.pdf, 40 Kb)</span>. Paper presented at the Third Conference of the European Historical Economics Society, Lisbon, October 29-30, 1999.<br><br>

Other datafiles relating to this paper:<br>
<a href="poynder-france.php">Monthly Grain Prices at Les Halles, Paris. 1549-1698</a><br> 
<a href="poynder-germany.php">Monthly Wheat Prices in Cologne, 1550-1700</a><br>
<a href="poynder-england.php">Monthly Grain Prices in England, 1270-1955</a></p>

<p>
The data have been collected from Giuseppe Parenti, <em>Prezzi e mercato del grano a Siena (1546-1765)</em> (Pubblicazioni della r. universit&agrave; degli studi di Firenze. Facolt&agrave; di economia e commercio XIX. Scuola di statistica) (Firenze, 1942), 38-41.</p>

<p>Weight: staio sense or 22.84 litres</p>

<p>Money: soldi of the lira (20 soldi per lira)</p>

<p>For international comparison, the silver content of the currency expressed in grams of silver is as follows:</p>

<pre>
   period	  lira	  soldi 
1542-1558	4,6480	0,23240
1559-1676	4,4576	0,22288
1677-1738	4,2838	0,21419
1739-1766	3,7824	0,18912</pre>

<p>Source: Parenti, <em>Prezzi e mercato del grano a Siena</em>, 30-37.</p>

