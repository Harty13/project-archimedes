<h2>Monthly Wheat Prices in Cologne, 1550-1700</h2>

<p><strong>The datafile:</strong> <a href="poynder-germany.xls">spreadsheet</a> <span class="small">(.xls, 36 Kb)</span></p>

<p>
This datafile was used in:<br>
Nicholas Poynder, <a href="poynder.pdf"><em>Grain storage in theory and history</em></a>, <span class="small">.(pdf, 40 Kb)</span>. Paper presented at the Third Conference of the European Historical Economics Society, Lisbon, October 29-30 (1999).</p> 

<p>
Other datafile relating to this paper:<br>
<a href="poynder-france.php">Monthly Grain Prices at Les Halles, Paris 1549-1698</a><br>
<a href="poynder-england.php">Monthly grain prices in England, 1270-1955</a><br>
<a href="poynder-italy.php">Monthly grain prices in Siena, 1550-1694</a></p>

<p>
The data on Cologne, used in above paper, were obtained from:<br>
D. Ebeling and F. Irsigler, <em>Mitteilungen aus dem stadtarchiv von K&ouml;ln: 1368-1797</em>, Cologne 1976, p.539-614.<br>
The data are given in Albus per malter, where:<br>
1 Gulden = 4 Mark = 24 Albus = 288 Heller<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1 Mark =&nbsp;&nbsp;&nbsp;6 Albus =&nbsp;&nbsp;&nbsp;72 Heller<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1 Albus =&nbsp;&nbsp;&nbsp;12 Heller<br>
and 1 malter wheat is ca. 164 litre.<br><br>

For people wanting to convert these prices in grams of silver, we provide the grams of silver per Albus in the table below.
</p>

