<p><em>Zeeland sources</em></p>

<p><em>Provinciaal Blad van Zeeland</em> (1830-56); <em>Verslag van Gedeputeerde Staten aan de Staten der Provincie Zeeland, Verslag van den Toestand der Provincie Zeeland door Gedeputeerde Staten uitgebragt aan de Provinciale Staten</em> (1856-76); <em>Bijdragen tot de Statistiek van Nederland, nieuwe volgreeks xvvi, Marktprijzen van Granen te Middelburg</em> (1904)*; RA Zeeland, <em>Archief Provinciaal Bestuur Zeeland 1813-50</em>, 282-283, 311, 313-317, 350, 356-360; ANP, <em>F11 Subsistences</em>, 839, 840, 842, 895; ARA, <em>Archief Binnenlandse Zaken 1795-1813</em>, 822, 981; ARA, <em>Archief Binnenlandse Zaken, Binnenlands Bestuur B, 1814-1831</em>, 1365;  ARA, <em>Geheim Archief Ministerie van Oorlog, 1813-1844, Achief van de Commissie van Onderzoek en Verevening der Pretensi&euml;n voortspruitende uit gedane Leveranti&euml;n aan de Geallieerde Legers, 1814-1832</em>, 64; RA Zeeland, <em>Prefectuur Archief 1810-1814</em>, 983, 987, 1116; <em>Verzameling van Stukken</em>, table G; Labrousse et al., <em>Prix du Froment</em>, 221.</p>

<p><em>Brabant sources</em></p>

<p><em>Provinciaal Blad van Noord-Braband</em> (1816-49); <em>Verslag van den Toestand der Provincie Noord-Brabant aan de Provinciale Staten aangeboden (1848-82)</em>; <em>Statistisch en Staathuishoudkundig Jaarboekje</em> (1860), 296; Van Mierlo (1988, 94-9); RA Noord-Brabant, <em>Archief Departementaal Bestuur van de Monden van de Rijn 1810-1814</em>, 1412, 1414; RA Noord-Brabant, <em>Archief Commissie voor Landbouw</em>, 44; GA Den Bosch, <em>Oud Archief</em>, 69; ARA, <em>Archief Binnenlandse Zaken, Binnenlands Bestuur B, 1814-1831</em>, 1365; ANP, <em>F11 Subsistences</em>, 839, 840, 842, 895; ARA, A<em>rchief Binnenlandse Zaken 1795-1813</em>, 822, 981; ARA, <em>Kabinet des Konings, 1813-1840</em>, 6512; <em>Verzameling van Stukken</em>, table G; Labrousse et al., <em>Prix du Froment</em>, 222; <em>BSI</em> (1889), 110; <em>JKN</em> (1881-1913).</p>

<p><em>Units of measurement</em></p>
<pre>
mud			100 litres ('dry' wares)
vat			100 litres ('wet' wares)
wisse			1 cubic metre
hl 			hectolitre; 100 litres
kg			kilogram; 1,000 grams
de 100			1,040 pieces</pre>

<p>Source: J.M. Verhoeff, <em>De oude Nederlandse maten en gewichten</em> (Amsterdam, 1982; Publikaties van het P.J. Meertens-Instituut voor dialectologie, volkskunde en naamkunde van de Koninklijke Nederlandse Akademie van Wetenschappen 3).</p>

<p><em>Currency</em></p>

<p>All prices are quoted in Dutch guilders, which consisted of 100 cents. The silver content of the guilder is available in Arthur van Riel, <a href="prijzen19earthur.xls">Prices of consumer and producer goods, 1800-1913</a></p>

