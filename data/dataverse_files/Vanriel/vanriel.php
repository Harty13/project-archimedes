<h2>Monthly prices of agricultural goods on Zeeland and Brabant markets, 1816-1855</h2>

<h5>Working document project on the reconstruction of the national accounts of the Netherlands<br>
Author: Arthur van Riel</h5>

<p>
<strong>The datafiles:</strong><br>
