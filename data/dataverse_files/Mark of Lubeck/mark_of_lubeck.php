<h2>Prices in Mark of L&uuml;beck (14th to 16th century)</h2>

<h5>Oliver Volckart</h5>
<strong>The datafile:</strong> <a href="mark_of_lubeck.xls">spreadsheet</a> <span class="small">(.xls, 2 Mb)</span>

<p>Most prices in the dataset are from a single source, i.e. the account books of the treasury of the city of Hamburg that span the period of time between 1350 and 1562 (Koppmann 1869; 1873; 1878; 1880; 1883 1892a, 1892b). Unfortunately, a number of the account books were lost in a fire that destroyed parts of Hamburg in the early 19th century. Some of the missing material could be reconstructed with the help of notes taken by a historian of the town in the years preceding the fire, but there are still large lacunae in the later 14th and early 15th centuries and at the beginning of the 16th century. A full list of all sources exploited is given in the References worksheet.</p>

<p>The prices in the dataset are sorted by categories of goods (grain, livestock, food, industrial products, metals and wages, each in one worksheet), and within the categories first alphabetically by individual goods and then chronologically. The columns give the date, the type of good, the quantity and price as stated in the source, the decimal equivalents of quantity and price, the price per 1 unit of measurement, the place and the source. The units of measurement found in the sources (and, where known, their metric equivalents) are given in the worksheet on Currencies, weights & measures.<br>
Finally, the worksheet on Yearly means gives the means of prices of those goods for which enough observations exist. The place is always Hamburg. The means are given both in nominal terms (i.e. in the mark of L&uuml;beck) per ancient unit of measurement and in grams of silver per a modern metric unit such as the kilo or the litre (the column to the far right gives the silver equivalent of the Mark of L&uuml;beck in grams).</p>
 
<p>The currency in which the prices are given is the Mark of L&uuml;beck. This was since the 13th century supplied by the cities of L&uuml;beck and Hamburg, and from 1379 by the so-called Wendish Monetary Union, a currency union that also comprised the cities of L&uuml;neburg, Wismar, and - briefly - other members such as Rostock, Stralsund, Hanover and even Denmark. The most authoritative account of the development of this currency is still Jesse (1928). Its structure is given in the worksheet on Currencies, weights & measures.</p>


