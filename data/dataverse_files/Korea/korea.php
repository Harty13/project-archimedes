<h2>Labour cost, land prices, land rent, and interest rates in the southern region of Korea (1690-1909)</h2>

<h5>Authors: S.H Jun & J.B Lewis</h5>
<p><strong>Background</strong><br> 
The gathering and analysis of quantifiable data on Korea's pre-modern history (pre-1850) is still in its heroic age. From the early 1990s, the document collection has grown and lately advances have been made in extracting data. In 2002, S.H. Jun and J.B. Lewis began a special project at the Academy of Korean Studies (AKS, web address: www.aks.ac.kr.) to create bibliographies of old documents and compile databases on prices and wages. The Academy, founded by the government of the Republic of Korea on June 30, 1978, is at the forefront of research and teaching on pre-modern and modern Korea in the fields of history, literature, and the social sciences. Its primary aim has been to overcome the ethical confusion that has emerged alongside rapid industrialization. In recent years, many private records and documents from post-1600 Korea have been gathered and published in photolithographic form. The Academy has been collecting, microfilming, and publishing private, non-governmental records for decades from its first publication in 1981. So far, the Academy has published volume 64 in the massive Komunso chipsong (Collected historical documents), but there has been little research on these documents from the perspective of economic history.<br><br>

