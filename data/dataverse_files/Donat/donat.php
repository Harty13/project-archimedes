<h2>Prices of cereals, butter and cheese in Brugge (1348-1801)</h2>

<h5>Author: A.E. Verhulst</h5>

<p>
<strong>The datafile:</strong> <a href="donat.xls">spreadsheet</a> <span class="small">(.xls, 120 Kb)</span></p>

<p>Prices are taken from the records of the Sint-Donatiaanskapittel in Brugge. This is one of the longest, consistent prices series available for early modern Europe, which also gives an impression of seasonal variation in prices. Verhulst, who published the prices series, shows that during the 15th century and again at the end of the 17th century they followed the market very closely, but that during the 16th century these prices were probably about 10% lower than at the market (p. 10).<br>
All prices are in <em>groten Vlaams</em> ; the silver value of the coin is given in the first column of the data file.<br>
The source is: C. Verlinden, Dokumenten voor de geschiedenis van prijzen en lonen in Vlaanderen en Brabant (XIVe &ndash; XIXe eeuw), vol II, Brugge 1965, pp. 3-70.</p>

