<h2>Sources of Javanese Rice Prices</h2>

<h5>Author: Jan Luiten van Zanden</h5>

<p>
<strong>The datafile:</strong> <a href="javarijstprijskort.xls">spreadsheet</a> <span class="small">(.xls, 56 Kb)</span></p>

<p>
The following is taken from Jan Luiten van Zanden, 'On the efficiency of Markets for Agricultural Products: Rice Prices and Capital Markets in Java, 1823-1853',  <em>The Journal of Economic History</em>, vol. 64, no. 4 (December 2004) 1028-1055; please refer to this paper when making use of the dataset.<br><br>

In 1823 the <em>Bataviasche Courant</em>, the official government gazette and newspaper of the merchant community of Batavia, began to publish market prices of a number of consumption goods: rice, firewood, coconut and peanut oil, and, at the same time, but in separate tables, the maximum prices of a number of European consumption goods (wheaten bread, pork and beef) as set by the local government. In 1824 and 1825 market prices in Semarang and Surabaya - the other important trading towns on the north coast of Java - were also included in these tables, and this continued when the newspaper was renamed into the <em>Javasche Courant</em> in 1828. The aim of these pricelists was clearly to increase the transparency of these markets for European buyers. In separate tables the <em>Javasche Courant</em> also published information on the import prices of commodities, sometimes in the form of ads from local merchants who announced that they had imported European commodities which could be bought at their warehouse, sometimes in the form of a price list to inform the general public.<sup>1</sup><br><br>

During most of the period under study (1823-1853) the journal appeared twice per week, on Wednesday and Saturday, and often one of these issues contained a price list. Sometimes, when many official publications had to be printed or for reasons that are unclear, no price lists were published during a certain week, and occasionally for more than a couple of weeks in a row. It is therefore not possible to construct weekly listings of these prices, but monthly quotations are no problem. I selected those prices that were closest to the middle (i.e. the 15th) of each month, but in some cases I had to use lists from for example the 4th  (December 1839) or the 22nd  (April 1826). During the first years the Batavia prices were often quoted as a range between minimum and maximum prices (for example 110 to 121 guilders on the 14th of May 1825), but after the middle of 1827 single prices became usual; when ranges were quoted I calculated the average between minimum and maximum. From January 1841 onwards three different qualities of rice were distinguished, although prices of all three classes were not always published. I concentrated on second-class rice, for which the price information was most abundant (this choice was also consistent with the calculation of an average between maximum and minimum prices before 1827).<br><br>

I already mentioned that there was some confusion about the measure of the koyang, which was used throughout the 1823-1855 period. The Surabaya and the Semarang koyang were somewhat larger than the Batavia koyang, but it is not entirely clear by how much, although it must have been in the range of 5 to 10%. During the early 1840s, when the currency chaos was reaching its apex, the price lists also state that they are based on payment in copper coins; all prices were expressed in Netherlands Indies guilders.<br>
The general impression from this source is that the quality of the information is high, mainly because the prices published changed very frequently. There are no reasons to assume that the <em>Javasche Courant</em>, or the sources used by them - the local merchants - had reasons to misrepresent the state of the rice market. Rice was, on the one hand, traded widely but was not a major commodity for the Dutch merchants who resided in the three cities, who concentrated on other export crops (coffee and sugar in particular). The prices from the <em>Javasche Courant</em> show the same trends as other price data from this period, in particular those from the export statistics.</p>

