<h2>Prices in &Ouml;sterg&ouml;tland, 1592-1735</h2>

<h5>Author: G&ouml;ran Hansson</h5>
<strong>The datafile</strong> : <a href="ostergotland.xls">spreadsheet</a> <span class="small">(.xls, 226 Kb)</span>

<h5>Introduction</h5>

<p>Data for the prices has been excerpted from four hospitals and about fifty parishes in &Ouml;sterg&ouml;tland. This area constituted as one of Sweden's core regions situated about 200 kilometres southwest of Stockholm, the capital of Sweden. The yearly prices are the medium of the prices for the actual harvest year. The nominal prices are expressed in Swedish dalers silver coins which is one of the three monetary systems during the most part of Sweden's Period as a Great power (cc 1592-1718). The other of the two currency systems for domestic use is Swedish dalers copper coins which in actual cases have been converted into silver coins. In order to show the prices expressed also in Swedish riksdaler (the then Swedish national currency) and in silver (grams), the prices are presented in separate price lists. To do it easier to make international comparisons the prices are also expressed in Hamburger thaler-banco (1658-1735) and Amsterdam rijksdaalder (1703-1735).</p>

