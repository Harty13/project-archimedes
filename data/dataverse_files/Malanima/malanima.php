<h2>Wheat prices in Tuscany, 1260-1660</h2>

<h5>Author: Paolo Malanima</h5>

<p>
<strong>The datafile:</strong> <a href="malanima.xls">spreadsheet</a> <span class="small">(.xls, 52 Kb)</span></p>

<p>
The series of Tuscan wheat prices is based on data for two cities: Pisa and Firenze. From 1470 on averages are grounded on monthly data: one for any month. The correlation between the wheat prices in these two cities is nearly perfect.</p>
<p>
<strong>Sources:</strong></p>
<p>
<strong>1260-1300 - Pisa</strong>: D. Herlihy, <em>Pisa nel Duecento</em>, Pisa, Nistri-Lischi, 1973 [I ed. 1958], pp. 234-35 (data concerning sales with postponed payment -actually disguised loans at interest- have been excluded; data are expressed in the original documents in Pisan staio -converted in the series in Florentine staio, at that time 35,9 percent of Pisan staio);<br>
<strong>1300-1548 - Firenze</strong>:R.A. Goldthwaite, <em>I prezzi del grano a Firenze dal XIV al XVI secolo</em>, in 'Quaderni storici', 28, 1975 (calculated again, when possible, per harvest year -July-June- and completed with data from Ch.M. De La Ronci&egrave;re, <em>Prix et salaires &agrave; Florence au XIVe si&egrave;cle</em>, Roma, Ecole Fran&ccedil;aise de Rome, 1982, App. 1, for the years 1336-1378; for the year 1369-70 the price is the one in Goldthwaite; for the years 1395-96, 1396-97 e 1412-13 data are from G. Pinto, <em>Il personale, le balie e i salariati salariati dell'ospedale di San Gallo di Firenze (1395-1406)</em>, in Id., <em>Toscana medievale. Paesaggi e realt&agrave; sociali</em>, Firenze, Le Lettere, 1993, p. 140 [I ed.1974];<br>
<strong>1548-1860 - Pisa</strong>: P. Malanima, <em>Aspetti di mercato e prezzi del grano e della segale a Pisa dal 1548 al 1818</em>, in <em>Ricerche di storia moderna</em>, I, Pisa, Pacini, 1976, pp. 288-327 (data after 1818 are from Archivio di Stato di Pisa, <em>Comune F</em>, 487-489 and have never been published before).</p>
<p>
Other Tuscan wheat series, used to estimate the correlation within the Granducato, are: for Siena from the 16<sup>th</sup> to the end of the 18<sup>th</sup> century in G. Parenti, <em>Prezzi e mercato del grano a Siena</em>, Firenze, CYA, 1942; for Firenze during the 18<sup>th</sup> century in O. Gori, <em>Mercato e prezzi del grano nel secolo XVIII</em> in 'Archivio storico italiano', CXLVII, 1989; for Prato in A. Menzione, <em>Agricoltura e propriet&agrave; fondiaria</em>, in <em>Prato. Storia di una citt&agrave;</em>, a c. di F. Braudel, 2, <em>Un microcosmo in movimento (1494-1815)</em>, a c. di E. Fasano Guarini, Firenze, Le Monnier, 1986., pp. 133-216, pp. 211-215.</p>
<p>
<strong>Notes:</strong></p>
<p>
When more quality of wheat appear in the sources -from 1548 on- the best has been chosen.</p>
<p>
Averages per harvest year: July-June.</p>
<p>
Wheight: staio fiorentino: 24,3629 liters; 18 kg.</p>
<p>
Money: soldi of Lira fiorentina (20 soldi per lira). Since, for international comparisons, it may be useful to know the weight of lira fiorentina in silver, here are collected data taken from R.A. Goldthwaite, <em>The building of Renaissance Florence</em>, Baltimore and London, The Johns Hopkins Univ. Press, 1980, pp. 429-30; M. Bernocchi, <em>Le monete della Repubblica fiorentina</em>, Firenze, 1974-78. When necessary, conversions from gold to silver are based on C.M. Cipolla, <em>Le avventure della lira</em>, Bologna, Il Mulino, 1975 [I ed.1958], p. 132.</p>
