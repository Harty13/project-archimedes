<h3>Introduction</h3>

<p>Twenty years ago guilds were decidedly out of fashion with historians. The accepted wisdom was that guilds were a European medieval phenomenon. As the guilds laid down specific rules for the production of goods and services, they stifled entrepreneurship and innovation. They therefore became less important in English towns already by the seventeenth century, and never were established in the America's. On the European continent their importance dwindled towards the end of the ancien r&eacute;gime, when economic corporations were toppled with the rest of the old order.</p>
<p>
Most of these certainties about guilds have been unable to withstand the impact of recent research, which in many countries has focussed on guilds once again. Comparative research in the Low Countries (Antwerp University, Free University Brussels, Utrecht University, International Institute of Social History) may serve as an example. New approaches in economic, social, labour and institutional history have re-examined guilds, not the least within the framework of a re-appraisal of the classic strict distinction between "capitalist" and "pre-capitalist" modes of production. They are unravelling the reasons why guilds were established, and why they could maintain themselves for such a long time. International comparisons have enhanced this rejuvenation of guild studies. Besides, awareness is growing that guilds are not just a European phenomenon, but have been prominent all over Northern Africa and the Middle East, as well as in many parts of Asia, including China and Japan. They have also existed in Latin America. In many countries guilds themselves were flourishing right up until the end of the ancien r&eacute;gime, in Central Europe they existed well into the nineteenth century and even into the twentieth century. </p>
<p>
Emphasis is now laid on the possibility that innovation, entrepreneurship, and at the same time social security could flourish within and outside of guilds. This was also the case outside Europe. In many Asian towns guilds competed with other forms of vertical organisation of the labour market, based on kin, caste, ethnicity, religion or place of birth. Some of these organisations and associations adopted functions and sported cultures that were close to those of the guilds. They not only organised access to a segment of the labour market, but offered mutual insurance, protection and family-like structures away from home, sponsored altars or temples and organised festivities. Described in these functional terms, guild-like organisations are not unknown in North America.</p>
<p>
A lot of these new insights are a result of a return to the sources. In some cases the products are databases on guilds and their activities. The growing importance of new institutional economics in the historical analysis of the pre industrial economy has also contributed to the reassessment of the role played by guilds. The question is no longer whether guilds were important or unimportant, backward or innovative, but rather under which circumstances they could play such a role; and internally under what circumstances those involved could reap the profits of their membership.
The organisers of the conference "The return of the Guild" feel that the time has come to take stock of the recent advances in the field, to compare the results in different countries and to organise new forms of international, cooperative and comparative research. The emphasis will be on global comparisons in order to break away from a narrow European debate and from Eurocentrism. They will bring together the leading historians of guilds from all over the globe. They will test whether a new coherent vision on guilds and their contribution to social-economic development is possible.</p>
<p> 
All participants have been requested to focus their case studies around the following four issues:</p>
<ol>
<li>The basics of "how many", "where" and "when" regarding "guilds". In this project we define guilds as permanent, generally local, organizations of people in the same profession (or a combination of the same professions), recognized by the (local, provincial or central) government, that have as their main (but certainly not exclusive) purpose the defence and maintenance of monopoly rights vis-&agrave;-vis co-citizens and outside competitors. Of course conceptual discussions about problems connected to the cross-cultural and cross-time applicability of the "guild"- concept will be part of this project, but always related to down-to-earth empirical research.</li>
 
<li>Questions regarding the function of guilds for their members: <br>
How did guilds regulate access to their trade, and how restrictive were those rules in theory and in practice?<br>
To what extent did women participate in guilds or develop their own guilds?<br>
To what extent did masters exploit journeymen and apprentices?<br>
To what extent were guild members equal; to what extent did some members sub-contract other members or outsiders?<br>
To what extent did guilds facilitate or regulate sociability (including partner choice) and social security for (the families of) their members?</li>

<li>Questions regarding the function of guilds for economy and society: <br>
How did guilds organize the training of apprentices and the transfer of skills and technology to the next generation more in general?<br>
Under what conditions did guilds suppress or stimulate innovations?<br>
What was the function of journeymen's migrations?<br>
How and to what extent did guilds regulate and stabilize labour markets? <br>
How important were different kinds of quality guarantees (such as brands and marks) supplied by guilds to the development of export markets for cities and regions and for international trade in general?</li>

<li>The political economy of guilds: <br>
Were guilds embedded in a civic - urban-based - political culture, or in a centralized state, and what were the consequences for this for the functioning of the guilds?<br>
When do states restrict, abolish, or forbid guilds?<br>
Which states regulate guilds directly or allow towns (or other sub-ordinate entities) to regulate their own guilds?</li>
</ol>

<p>The conference will also promote comparative international research by stimulating the construction of national databases of guilds (as do exist already for e.g. Belgium, Italy, the Netherlands, and parts of the Austrian Empire), and the international exchange of data through the establishment of a hub on the Internet. It will result in an extensive publication, which we hope will stimulate international comparative research into guilds for a number of years to come.
</p>

