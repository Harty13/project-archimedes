<h2>Lonen en prijzen in het cisterci&euml;nzerinnenklooster Leeuwenhorst bij Noordwijkerhout tussen 1410/11 en 1570/71</h2>

<h5>Author: Dr. Geertruida de Moor</h5>

<p>
The convent Leeuwenhorst near Noordwijkerhout existed from 1261 till 1574. As it was very wealthy bought a great variety of products and services of laborers. Therefore it is a very important guide into the historical prices and wages in the Netherlands. The convent offers a wealth of data because the accounts have survived relatively complete. The accounts from the years 1411/12, 1413/14, 1415/16, 1421/22, 1422/23, 1423/24, 1526/27, 1547/48 and 1550/51 are missing while some other years are partially damaged. They were written in duplicate. The financial year varied but mostly started on Sint-Laurentius (10 August), on Valkenburger- and/or Voorschotermarkt (around the midst of September) or on Bamis (Sint-Bavo, 1 October) and ended at the same date the next year.<br>
For bookkeeping a fictional money was used, the so-called <i>rekenmunt</i>, because the real money often varied in value. A <i>rekenmunt</i> consisted of a <i>pond</i>, which was divided in 20 <i>schellingen</i>, and every <i>schelling</i> in 12 <i>denieren</i>. Therefore a <i>pond</i> consisted of 240 <i>denieren</i>. There circulated three different sorts of <i>rekenmunten</i> but because the vast amount of the accounts were written in <i>Comans payment</i>, everything is converted to <i>denieren</i> of <i>Comans payment</i>. In <i>Comans payment</i> a <i>pond</i> consisted of 15 <i>stuivers</i> (or 30 <i>groot</i>), a <i>stuiver</i> of 16 <i>penningen</i> and a <i>groot</i> of 8. A <i>pond Comans payment</i> consisted therefore of 240 <i>penningen</i>. In comparison to the <i>rekenmunt</i> earlier mentioned, a <i>penning</i> was equal in value to a <i>denier</i>. For comparison, the silver value of a <i>penning</i> is given in the first column of the data file for the years concerned.</p> 

