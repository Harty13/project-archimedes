<h2>Wages and the cost of living in Southern England (London) 1450-1700</h2>

<h5>Author: Jan Luiten van Zanden</h5>

<p>
<strong>The datafile:</strong> <a href="dover.xls">spreadsheet</a> <span class="small">(.xls, 120 Kb)</span></p>

<h5>Wages</h5>
<p>The Phelps Brown & Hopkins (PBH) index is based on a rather static interpretation of the data on wages of carpenters and labourers in Cambridge and Oxford presented by Thorold Rogers. In my view, the Rogers data suggest more flexibility than the PBH summaries of them. An example is that PBH give a constant wage of 12 pence for carpenters and of 8 pence for labourers between 1580 and 1626, whereas the Rogers data show that already in 1611 14 d. and 10 d. had become normal in Cambridge (although wages of 9 d. also occured during the 1620s). I have followed the Rogers data more closely. More importantly, I was able to include similar wage series from Dover, and Canterbury, and the London data from Rappaport and Boulton (1996). All other data wage data were derived from the data of the unpublished volumes of History of Wages and Prices in England, collected by Beveridge, and now deposited at the library of the LSE. The relevant files are: W3 and W8 (wages in London in 16th and 17th century), D4 and D5 (Canterbury), W2 and W7 (Dover, additional data on Oxford and Cambridge). I have calculated unweighted averages on both sets of five series to derive two series of average wages of carpenters and of labourers, and again taken the average of both series to construct the wage index for the period 1450-1700. A comparison between this series and the PBH series shows that they seriously underestimated the rise of nominal wages between 1560 and 1640 (in the 1610s the difference is as much as 30% of the PBH-level); not only the London wages increased much more rapidly than the PBH estimates indicate, but this is also true for wages in Cambridge, Dover and Exeter. Oxford, on which the PBH series for this period are largely based, seems to be exceptional.</p>

<h5>Consumer Price Index</h5>
<p>The CPI constructed by Rappaport (1490-1609, base period 1457/71) was linked to the 'Vanderlint- labouring sort' index constructed by Boulton (2000) (I thank Jeremy Boulton for making this series available).<br>
<br>
These series will be published and discussed in detail in Van Zanden (manuscript)</p>

